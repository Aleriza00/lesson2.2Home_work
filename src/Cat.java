public class Cat {
    private  String name;
   private  int age;
   private int babies;
   private  String eats;

    public void setName(String name ){
        this.name= name;
    }
    public  String getName(){
        return  name;
    }
    public  void setAge(int age){
        this.age = age ;
    }
    public  int getAge(){
        return  age;
    }
    public void setBabies(int babies){
        this.babies = babies;
    }
    public int getBabies(){
        return babies;
    }

    public void  setEats(String eats){
        this.eats= eats;
    }
    public String getEats(){
        return  eats;
    }

}
